public class VariaveisExemplo {
    public static void main(String[] args) {
        // Declaração de variáveis
        int numeroInteiro = 42;
        double numeroFlutuante = 3.14;
        String texto = "Olá, mundo!";

        // Exibição das variáveis na tela
        System.out.println("Número inteiro: " + numeroInteiro);
        System.out.println("Número flutuante: " + numeroFlutuante);
        System.out.println("Texto: " + texto);
    }
}
